// Функція для отримання свят за категорією
export const getHolidaysByCategory = (category) => {
  const holidays = [];
  
  Object.keys(ukrainianHolidays).forEach(dateKey => {
    const holiday = ukrainianHolidays[dateKey];
    if (holiday.category === category) {
      holidays.push({
        date: dateKey,
        ...holiday
      });
    }
  });
  
  return holidays.sort((a, b) => a.date.localeCompare(b.date));
};

// Функція для отримання найближчих свят
export const getUpcomingHolidays = (daysAhead = 30) => {
  const today = new Date();
  const endDate = new Date(today.getTime() + daysAhead * 24 * 60 * 60 * 1000);
  const holidays = [];
  
  Object.keys(ukrainianHolidays).forEach(dateKey => {
    const holidayDate = new Date(dateKey);
    if (holidayDate >= today && holidayDate <= endDate) {
      holidays.push({
        date: dateKey,
        ...ukrainianHolidays[dateKey]
      });
    }
  });
  
  return holidays.sort((a, b) => a.date.localeCompare(b.date));
};

